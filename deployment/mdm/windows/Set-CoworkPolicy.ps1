$ErrorActionPreference = 'Stop'

# ===== CONFIGURATION — EDIT THESE VALUES =====

$config = @{
    # Required: Inference provider (always "bedrock" for this solution)
    inferenceProvider       = "bedrock"

    # Required: AWS region with Bedrock model access
    inferenceBedrockRegion  = "us-east-1"

    # Required: AWS CLI profile name (must match ~/.aws/config stanza with credential_process)
    inferenceBedrockProfile = "ClaudeCode"

    # Required: Available model aliases (JSON array)
    inferenceModels         = '["opus","sonnet","haiku"]'

    # Feature toggles
    isClaudeCodeForDesktopEnabled       = "true"
    isDesktopExtensionEnabled           = "true"
    isDesktopExtensionDirectoryEnabled  = "true"
    isDesktopExtensionSignatureRequired = "true"
    isLocalDevMcpEnabled                = "true"

    # Optional: OTLP telemetry (uncomment to enable)
    # otlpEndpoint = "http://localhost:4318"
    # otlpProtocol = "http/protobuf"
    # otlpHeaders  = '{"X-Cowork-Token": "your-service-token"}'
}

# ===== END CONFIGURATION =====

$regPath = "HKCU:\SOFTWARE\Policies\Claude"

# Create registry key if it doesn't exist
if (-not (Test-Path $regPath)) {
    New-Item -Path $regPath -Force | Out-Null
}

# Write each configuration value as REG_SZ
foreach ($key in $config.Keys) {
    $value = $config[$key]
    if ($null -ne $value -and $value -ne "") {
        Set-ItemProperty -Path $regPath -Name $key -Value $value -Type String
        Write-Output "  Set $key"
    }
}

Write-Output "`nClaude Cowork 3P policy deployed to $regPath"
Write-Output "Restart Claude Desktop to apply changes."
