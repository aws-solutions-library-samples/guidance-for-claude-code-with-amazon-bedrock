import * as cdk from 'aws-cdk-lib';
import * as cloudfront from 'aws-cdk-lib/aws-cloudfront';
import * as origins from 'aws-cdk-lib/aws-cloudfront-origins';
import * as s3 from 'aws-cdk-lib/aws-s3';
import * as lambda from 'aws-cdk-lib/aws-lambda';
import * as apigateway from 'aws-cdk-lib/aws-apigateway';
import * as iam from 'aws-cdk-lib/aws-iam';
import * as logs from 'aws-cdk-lib/aws-logs';
import * as cognito from 'aws-cdk-lib/aws-cognito';
import * as secretsmanager from 'aws-cdk-lib/aws-secretsmanager';
import { Construct } from 'constructs';
import { LandingPageConfig } from './config';
import * as path from 'path';

export interface CloudFrontLandingPageStackProps extends cdk.StackProps {
  config: LandingPageConfig;
  userPool: cognito.UserPool;
  userPoolClient: cognito.UserPoolClient;
  userPoolDomain: cognito.UserPoolDomain;
}

/**
 * Secure Landing Page Stack using CloudFront + API Gateway + Lambda
 *
 * Security:
 * - Lambda is NOT public - only accessible via API Gateway
 * - API Gateway requires API Key authentication
 * - Only CloudFront has the API Key (passed as custom header)
 * - S3 uses Origin Access Control (only CloudFront can access)
 */
export class CloudFrontLandingPageStack extends cdk.Stack {
  public readonly distributionBucket: s3.Bucket;
  public readonly distribution: cloudfront.Distribution;
  public readonly landingPageUrl: string;

  constructor(scope: Construct, id: string, props: CloudFrontLandingPageStackProps) {
    super(scope, id, props);

    const { config, userPool, userPoolClient, userPoolDomain } = props;
    const prefix = config.profileName;

    // S3 bucket for packages (private, CloudFront OAC only)
    this.distributionBucket = new s3.Bucket(this, 'DistributionBucket', {
      bucketName: `${prefix}-packages-${this.account}-${this.region}`.toLowerCase().substring(0, 63),
      encryption: s3.BucketEncryption.S3_MANAGED,
      blockPublicAccess: s3.BlockPublicAccess.BLOCK_ALL,
      versioned: true,
      removalPolicy: cdk.RemovalPolicy.DESTROY,
      autoDeleteObjects: true,
    });

    // Secret used to HMAC-sign the session cookie so it can't be forged.
    // Auto-generated by Secrets Manager; the Lambda only ever reads it.
    const sessionSigningSecret = new secretsmanager.Secret(this, 'SessionSigningSecret', {
      secretName: `${prefix}-session-signing-secret`,
      generateSecretString: {
        passwordLength: 64,
        excludePunctuation: true,
      },
      removalPolicy: cdk.RemovalPolicy.DESTROY,
    });

    // Lambda for landing page (NOT public - only API Gateway can invoke)
    const landingPageFunction = new lambda.Function(this, 'LandingPageFunction', {
      functionName: `${prefix}-landing-page`,
      runtime: lambda.Runtime.PYTHON_3_12,
      handler: 'index.lambda_handler',
      timeout: cdk.Duration.seconds(30),
      memorySize: 512,
      environment: {
        S3_BUCKET_NAME: this.distributionBucket.bucketName,
        COGNITO_DOMAIN: `${userPoolDomain.domainName}.auth.${this.region}.amazoncognito.com`,
        COGNITO_CLIENT_ID: userPoolClient.userPoolClientId,
        COGNITO_USER_POOL_ID: userPool.userPoolId,
        COGNITO_BOOTSTRAP_CLIENT_ID: config.bootstrapOidcClientId || '',
        REGION: this.region,
        IDC_INSTANCE_ARN: config.idcInstanceArn,
        ADMIN_GROUP: 'Claude-Code-Admins',
        SESSION_SIGNING_SECRET_ARN: sessionSigningSecret.secretArn,
      },
      code: lambda.Code.fromAsset(path.join(__dirname, '..', 'lambda')),
      logRetention: logs.RetentionDays.ONE_MONTH,
    });

    // Grant Lambda access to S3 (read/write for admin config updates)
    this.distributionBucket.grantReadWrite(landingPageFunction);

    // Grant Lambda read access to the session-signing secret
    sessionSigningSecret.grantRead(landingPageFunction);

    // Grant Lambda permissions for IAM Identity Center operations (admin)
    landingPageFunction.addToRolePolicy(new iam.PolicyStatement({
      actions: [
        'sso:ListInstances',
        'sso:ListPermissionSets',
        'sso:DescribePermissionSet',
        'sso:CreatePermissionSet',
        'sso:PutInlinePolicyToPermissionSet',
        'sso:GetInlinePolicyForPermissionSet',
        'sso:ListAccountAssignments',
        'sso:CreateAccountAssignment',
        'sso:ProvisionPermissionSet',
        'sso:DescribePermissionSetProvisioningStatus',
        'sso-admin:ListInstances',
        'sso-admin:ListPermissionSets',
        'sso-admin:DescribePermissionSet',
        'sso-admin:CreatePermissionSet',
        'sso-admin:PutInlinePolicyToPermissionSet',
        'sso-admin:GetInlinePolicyForPermissionSet',
        'sso-admin:ListAccountAssignments',
        'sso-admin:CreateAccountAssignment',
        'sso-admin:ProvisionPermissionSet',
        'sso-admin:DescribePermissionSetProvisioningStatus',
        'identitystore:ListGroups',
        'identitystore:DescribeGroup',
        'identitystore:ListUsers',
        'identitystore:ListGroupMembershipsForMember',
      ],
      resources: ['*'],
    }));

    // Grant Lambda permissions for Bedrock model discovery
    landingPageFunction.addToRolePolicy(new iam.PolicyStatement({
      actions: [
        'bedrock:ListFoundationModels',
        'bedrock:GetFoundationModel',
        'bedrock:ListInferenceProfiles',
      ],
      resources: ['*'],
    }));

    // API Gateway REST API with API Key requirement
    const api = new apigateway.RestApi(this, 'LandingPageApi', {
      restApiName: `${prefix}-landing-page-api`,
      description: 'Landing page API - requires API key',
      deployOptions: {
        stageName: 'prod',
        throttlingBurstLimit: 100,
        throttlingRateLimit: 50,
      },
      // Require API key for all methods
      apiKeySourceType: apigateway.ApiKeySourceType.HEADER,
    });

    // Lambda integration
    const lambdaIntegration = new apigateway.LambdaIntegration(landingPageFunction, {
      proxy: true,
    });

    // Root resource with API key required
    api.root.addMethod('GET', lambdaIntegration, {
      apiKeyRequired: true,
    });

    // Proxy resource for all paths (callback, download, etc.)
    const proxyResource = api.root.addResource('{proxy+}');
    proxyResource.addMethod('GET', lambdaIntegration, {
      apiKeyRequired: true,
    });
    proxyResource.addMethod('POST', lambdaIntegration, {
      apiKeyRequired: true,
    });

    // Create API Key (CloudFront will use this)
    const apiKey = api.addApiKey('CloudFrontApiKey', {
      apiKeyName: `${prefix}-cloudfront-key`,
      description: 'API key for CloudFront to access landing page',
    });

    // Usage plan to associate API key with API
    const usagePlan = api.addUsagePlan('UsagePlan', {
      name: `${prefix}-usage-plan`,
      throttle: {
        rateLimit: 100,
        burstLimit: 200,
      },
    });
    usagePlan.addApiKey(apiKey);
    usagePlan.addApiStage({
      stage: api.deploymentStage,
    });

    // Get the API key value for CloudFront
    // We need to use a Custom Resource to retrieve the actual key value
    const getApiKeyValue = new cdk.custom_resources.AwsCustomResource(this, 'GetApiKeyValue', {
      onCreate: {
        service: 'APIGateway',
        action: 'getApiKey',
        parameters: {
          apiKey: apiKey.keyId,
          includeValue: true,
        },
        physicalResourceId: cdk.custom_resources.PhysicalResourceId.of(apiKey.keyId),
      },
      policy: cdk.custom_resources.AwsCustomResourcePolicy.fromStatements([
        new iam.PolicyStatement({
          actions: ['apigateway:GET'],
          resources: [`arn:aws:apigateway:${this.region}::/apikeys/${apiKey.keyId}`],
        }),
      ]),
      logRetention: logs.RetentionDays.ONE_DAY,
    });

    const apiKeyValue = getApiKeyValue.getResponseField('value');

    // Origin Access Control for S3
    const s3Oac = new cloudfront.S3OriginAccessControl(this, 'S3OAC', {
      originAccessControlName: `${prefix}-s3-oac`,
    });

    // CloudFront Distribution
    this.distribution = new cloudfront.Distribution(this, 'Distribution', {
      comment: `Claude Code Landing Page - ${prefix}`,
      priceClass: cloudfront.PriceClass.PRICE_CLASS_100,
      defaultBehavior: {
        origin: new origins.HttpOrigin(`${api.restApiId}.execute-api.${this.region}.amazonaws.com`, {
          originPath: '/prod',
          customHeaders: {
            'x-api-key': apiKeyValue,
          },
          protocolPolicy: cloudfront.OriginProtocolPolicy.HTTPS_ONLY,
        }),
        viewerProtocolPolicy: cloudfront.ViewerProtocolPolicy.REDIRECT_TO_HTTPS,
        allowedMethods: cloudfront.AllowedMethods.ALLOW_ALL,
        cachePolicy: cloudfront.CachePolicy.CACHING_DISABLED,
        originRequestPolicy: cloudfront.OriginRequestPolicy.ALL_VIEWER_EXCEPT_HOST_HEADER,
      },
      additionalBehaviors: {
        '/packages/*': {
          origin: origins.S3BucketOrigin.withOriginAccessControl(this.distributionBucket, {
            originAccessControl: s3Oac,
          }),
          viewerProtocolPolicy: cloudfront.ViewerProtocolPolicy.REDIRECT_TO_HTTPS,
          allowedMethods: cloudfront.AllowedMethods.ALLOW_GET_HEAD,
          cachePolicy: cloudfront.CachePolicy.CACHING_OPTIMIZED,
        },
      },
    });

    this.landingPageUrl = `https://${this.distribution.distributionDomainName}`;

    // Update Lambda environment with CloudFront domain
    new cdk.custom_resources.AwsCustomResource(this, 'UpdateLambdaEnv', {
      onCreate: {
        service: 'Lambda',
        action: 'updateFunctionConfiguration',
        parameters: {
          FunctionName: landingPageFunction.functionName,
          Environment: {
            Variables: {
              S3_BUCKET_NAME: this.distributionBucket.bucketName,
              COGNITO_DOMAIN: `${userPoolDomain.domainName}.auth.${this.region}.amazoncognito.com`,
              COGNITO_CLIENT_ID: userPoolClient.userPoolClientId,
              COGNITO_USER_POOL_ID: userPool.userPoolId,
              COGNITO_BOOTSTRAP_CLIENT_ID: config.bootstrapOidcClientId || '',
              REGION: this.region,
              CLOUDFRONT_DOMAIN: this.distribution.distributionDomainName,
              IDC_INSTANCE_ARN: config.idcInstanceArn,
              ADMIN_GROUP: 'Claude-Code-Admins',
              SESSION_SIGNING_SECRET_ARN: sessionSigningSecret.secretArn,
            },
          },
        },
        physicalResourceId: cdk.custom_resources.PhysicalResourceId.of(`${landingPageFunction.functionName}-env-update`),
      },
      onUpdate: {
        service: 'Lambda',
        action: 'updateFunctionConfiguration',
        parameters: {
          FunctionName: landingPageFunction.functionName,
          Environment: {
            Variables: {
              S3_BUCKET_NAME: this.distributionBucket.bucketName,
              COGNITO_DOMAIN: `${userPoolDomain.domainName}.auth.${this.region}.amazoncognito.com`,
              COGNITO_CLIENT_ID: userPoolClient.userPoolClientId,
              COGNITO_USER_POOL_ID: userPool.userPoolId,
              COGNITO_BOOTSTRAP_CLIENT_ID: config.bootstrapOidcClientId || '',
              REGION: this.region,
              CLOUDFRONT_DOMAIN: this.distribution.distributionDomainName,
              IDC_INSTANCE_ARN: config.idcInstanceArn,
              ADMIN_GROUP: 'Claude-Code-Admins',
              SESSION_SIGNING_SECRET_ARN: sessionSigningSecret.secretArn,
            },
          },
        },
        physicalResourceId: cdk.custom_resources.PhysicalResourceId.of(`${landingPageFunction.functionName}-env-update`),
      },
      policy: cdk.custom_resources.AwsCustomResourcePolicy.fromStatements([
        new iam.PolicyStatement({
          actions: ['lambda:UpdateFunctionConfiguration'],
          resources: [landingPageFunction.functionArn],
        }),
      ]),
      logRetention: logs.RetentionDays.ONE_DAY,
    });

    // Outputs
    new cdk.CfnOutput(this, 'LandingPageUrl', {
      value: this.landingPageUrl,
      description: 'Landing page URL (CloudFront)',
      exportName: `${prefix}-LandingPageUrl`,
    });

    new cdk.CfnOutput(this, 'DistributionBucketName', {
      value: this.distributionBucket.bucketName,
      description: 'S3 bucket for packages',
      exportName: `${prefix}-DistributionBucket`,
    });

    new cdk.CfnOutput(this, 'CloudFrontDistributionId', {
      value: this.distribution.distributionId,
      description: 'CloudFront distribution ID',
      exportName: `${prefix}-DistributionId`,
    });

    new cdk.CfnOutput(this, 'ApiGatewayUrl', {
      value: api.url,
      description: 'API Gateway URL (requires API key - do not access directly)',
      exportName: `${prefix}-ApiGatewayUrl`,
    });

    new cdk.CfnOutput(this, 'SessionSigningSecretArn', {
      value: sessionSigningSecret.secretArn,
      description: 'Secrets Manager ARN of the HMAC key used to sign session cookies',
      exportName: `${prefix}-SessionSigningSecretArn`,
    });
  }
}
